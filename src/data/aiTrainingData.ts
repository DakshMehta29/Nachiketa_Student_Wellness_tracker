// Function to get random training questions for a specific companion
export const getRandomTrainingQuestions = (companionType: string, count: number = 10) => {
  const questions = allTrainingData[companionType as keyof typeof allTrainingData];
  if (!questions) return [];
  
  const shuffled = [...questions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Function to get all training questions for a specific companion
export const getAllTrainingQuestions = (companionType: string) => {
  return allTrainingData[companionType as keyof typeof allTrainingData] || [];
};
